#include <iostream>
using namespace std;

// Write a program to declare and initialize variables of different data types and print their values
int main() {
    int num = 10;
    float pi = 3.14;
    char grade = 'A';
    string name = "Dipu";
    bool status = true;

    cout << "Integer: " << num << endl;
    cout << "Float: " << pi << endl;
    cout << "Char: " << grade << endl;
    cout << "String: " << name << endl;
    cout << "Boolean: " << status << endl;

    return 0;
}